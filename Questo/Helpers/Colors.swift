import UIKit

extension UIColor {
    
    @nonobjc class var magenta: UIColor {
        return UIColor(red: 205.0 / 255.0, green: 0.0, blue: 171.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var purplyPink: UIColor {
        return UIColor(red: 240.0 / 255.0, green: 108.0 / 255.0, blue: 1.0, alpha: 1.0)
    }
    
    @nonobjc class var black: UIColor {
        return UIColor(white: 0.0, alpha: 1.0)
    }
    
    @nonobjc class var greyishBrown: UIColor {
        return UIColor(white: 74.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var brownGrey: UIColor {
        return UIColor(white: 155.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var brightBlue: UIColor {
        return UIColor(red: 0.0, green: 102.0 / 255.0, blue: 1.0, alpha: 1.0)
    }
    
    @nonobjc class var vibrantBlue: UIColor {
        return UIColor(red: 9.0 / 255.0, green: 47.0 / 255.0, blue: 1.0, alpha: 1.0)
    }
    
    @nonobjc class var purpleishBlue: UIColor {
        return UIColor(red: 89.0 / 255.0, green: 71.0 / 255.0, blue: 241.0 / 255.0, alpha: 1.0)
    }
    
}

